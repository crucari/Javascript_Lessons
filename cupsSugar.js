// Create a do...while loop which increments cupsAdded by one 
// while cupsAdded is less than cupsOfSugarNeeded.

let cupsOfSugarNeeded = 3;
let cupsAdded = 0;

do {
 cupsAdded++
} while (cupsAdded < cupsOfSugarNeeded);

